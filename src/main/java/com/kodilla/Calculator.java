package com.kodilla;

public class Calculator {
    private int a;
    private int b;

    public void Calculator() {
        this.a = a;
        this.b = b;
    }

    public void addition(int a, int b) {
        System.out.println(a+b);
    }

    public void subtraction(int a, int b) {
        System.out.println(a-b);
    }
}
